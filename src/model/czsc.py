class CZSC:
    """
    缠中说禅 (CZSC) K线处理类
    目前实现：merge_candlesticks - 消除K线包含关系
    """
    
    @staticmethod
    def merge_candlesticks(df: pd.DataFrame) -> pd.DataFrame:
        """
        对传入的K线DataFrame进行缠论K线合并（消除包含关系）
        
        参数:
            df: 必须包含列 ['index', 'open', 'high', 'low', 'close']
                'index' 用于保留原始序号
        
        返回:
            合并后的DataFrame，index列保留原始位置，行数减少
        """
        if df.empty or len(df) < 2:
            return df.copy()
        
        # 转为列表方便处理，每根K线为 dict
        candles: List[Dict] = df.to_dict('records')
        merged: List[Dict] = []
        
        for candle in candles:
            current = {
                'index': candle['index'],
                'open': candle['open'],
                'high': candle['high'],
                'low': candle['low'],
                'close': candle['close']
            }
            
            while merged:
                prev = merged[-1]
                
                # 判断是否包含（包含关系）
                if (current['high'] >= prev['high'] and current['low'] <= prev['low']) or \
                   (current['high'] <= prev['high'] and current['low'] >= prev['low']):
                    
                    # 执行合并：新开盘 = 前开盘，新收盘 = 当前收盘
                    new_high = max(prev['high'], current['high'])
                    new_low = min(prev['low'], current['low'])
                    
                    merged[-1] = {
                        'index': prev['index'],      # 保留前一根的index
                        'open': prev['open'],        # 开盘用前一根
                        'high': new_high,
                        'low': new_low,
                        'close': current['close']    # 收盘用当前这根
                    }
                    # 继续检查是否还能和更前面合并
                    current = merged.pop()  # 把刚合并的结果拿出来继续判断
                else:
                    break  # 无包含，停止合并
            
            merged.append(current)
        
        # 转回DataFrame
        result = pd.DataFrame(merged)
        # 保持原始列顺序
        return result[['index', 'open', 'high', 'low', 'close']]
    

    @staticmethod
    def plot_merge_comparison(original_df: pd.DataFrame, merged_df: pd.DataFrame, 
                              title: str = "Merge Comparison", figsize=(14, 8)):
        """
        绘制合并前后的K线图对比（上下两张图）
        
        参数:
            original_df: 原始DataFrame（必须包含 index, open, high, low, close）
            merged_df:   合并后的DataFrame
        """
        fig, (ax1, ax2) = plt.subplots(2, 1, figsize=figsize, sharex=False, height_ratios=[1, 1])
        fig.suptitle(title, fontsize=16, fontweight='bold')
        
        def plot_candles(ax, df, title_text):
            # 准备OHLC数据，x轴使用原始index（为了清晰对比）
            quotes = []
            for i, row in df.iterrows():
                quotes.append((row['index'], row['open'], row['high'], row['low'], row['close']))
            
            # 绘制K线
            candlestick_ohlc(ax, quotes, width=0.6, colorup='red', colordown='green', alpha=0.8)
            
            ax.set_title(title_text, fontsize=14)
            ax.set_ylabel('Price')
            ax.grid(True, alpha=0.3)
            
            # x轴显示原始index
            ax.set_xticks(df['index'])
            ax.set_xticklabels(df['index'].astype(int), rotation=45)
            
            # 美化
            ax.spines['top'].set_visible(False)
            ax.spines['right'].set_visible(False)
        
        # 上图：原始K线
        plot_candles(ax1, original_df, f"Original Candlesticks: {len(original_df)}")
        
        # 下图：合并后K线
        plot_candles(ax2, merged_df, f"Merged Candlesticks: {len(merged_df)}")
        
        plt.tight_layout()
        plt.show()


